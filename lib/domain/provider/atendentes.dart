import 'package:flutter/material.dart';
import 'package:projeto_desafio_flutter/infrastructure/data.dart';
import '../models/atendente.dart';

class Atendentes with ChangeNotifier {
  final List<Atendente> _items = [...Mock().atendentes];

  List<Atendente> get all {
    return [..._items];
  }

  List<Atendente> sortList() {
    List<Atendente> listAtendents = [..._items];
    List<Atendente> ordenado = listAtendents.toList();
    ordenado.sort((a, b) => a.nome.compareTo(b.nome));
    return ordenado;
  }

  int get count {
    return _items.length;
  }

  Atendente byIndex(int i) {
    return sortList().elementAt(i);
  }

  void put(Atendente atendente) {
    if (_items.contains(atendente)) {
      _items.remove(atendente);
      _items.add(Atendente(nome: atendente.nome, comissao: atendente.comissao, id: atendente.id));
    } else {
      _items.add(atendente);
    }
    notifyListeners();
  }

  void remove(Atendente atendente) {
    _items.remove(atendente);
    notifyListeners();
  }
}




























































































































































































































































































































































































































